export function categorize(title: string, sourceCategory?: string): string {
  if (sourceCategory && SOURCE_MAP[sourceCategory.trim()]) return SOURCE_MAP[sourceCategory.trim()];
  for (const [cat, re] of KEYWORDS) if (re.test(title)) return cat;
  return "기타";
}
